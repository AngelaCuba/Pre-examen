package Ejercicio3FactoryM;

public class Compu {
    private String marca;
    private String os;

    public Compu() {}


    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }
}
